function makeReaders(count) {
  const readers = []
  const seen = new Set()
  while (readers.length < count) {
    const s = pick(SURNAMES)
    const g = pick(GIVEN)
    const username = `${PINYIN[s]}${GPY[g]}${readers.length}`
    if (seen.has(username)) continue
    seen.add(username)
    readers.push({
      username,
      name: s + g,
      type: rand() < 0.3 ? 'TEACHER' : 'STUDENT',
      sex: rand() < 0.5 ? '男' : '女',
      birth: `${1975 + Math.floor(rand() * 30)}-${String(1 + Math.floor(rand() * 12)).padStart(2, '0')}-${String(1 + Math.floor(rand() * 28)).padStart(2, '0')}`,
    })
  }
  return readers
}

function main(books) {
  const readers = makeReaders(34)
  const lines = []
  lines.push('-- Generated by tools/expand-catalog/generate.mjs — real Open Library bibliographic')
  lines.push('-- records; fictional demo readers; deterministic 12-month circulation history')
  lines.push('-- (new books × new readers only, so the original test dataset is untouched).')
  lines.push('')

  // books
  lines.push('INSERT INTO books (title, author, publisher, isbn, intro, language, price_cents, pub_date, category_id, cover_url) VALUES')
  const bookValues = books.map((b) =>
    ` ('${esc(b.title)}', '${esc(b.author)}', '${esc(b.publisher)}', '${b.isbn}', NULL, '英文', NULL, ${b.year ? `'${b.year}-01-01'` : 'NULL'}, ${b.categoryId}, 'https://covers.openlibrary.org/b/isbn/${b.isbn}-M.jpg')`)
  lines.push(bookValues.join(',\n') + ';')
  lines.push('')

  // copies: 2 per new book, barcodes from barcode_seq
  lines.push(`INSERT INTO book_copies (book_id, barcode, call_number, location, status)
SELECT b.id,
       'LB' || lpad(nextval('barcode_seq')::text, 6, '0'),
       c.code || '-' || lpad(b.id::text, 4, '0') || '-' || s.n,
       CASE WHEN c.code IN ('I','J','K') THEN '总馆二层人文区'
            WHEN c.code = 'T' THEN '总馆三层科技区'
            ELSE '总馆一层综合区' END,
       'IN_LIBRARY'
FROM books b
JOIN categories c ON c.id = b.category_id
CROSS JOIN (SELECT 1 AS n UNION ALL SELECT 2) s
WHERE b.id > 62;`)
  lines.push('')

  // readers
  lines.push('INSERT INTO users (username, password_hash, display_name, role, email, preferred_locale, email_verified) VALUES')
  lines.push(readers.map((r) =>
    ` ('${r.username}', '$2y$10$phcewVRi9GOjNPOny9yYDeLl6yEAJbsqRuns8yb3eYxIJ2NNhwxiG', '${r.name}', 'READER', '${r.username}@libris.local', 'zh-CN', true)`).join(',\n') + ';')
  lines.push('')
  lines.push('INSERT INTO reader_profiles (user_id, reader_type, sex, birth, address)')
  lines.push('SELECT u.id, v.rt, v.sex, v.birth::date, v.addr FROM users u JOIN (VALUES')
  lines.push(readers.map((r) =>
    ` ('${r.username}', '${r.type}', '${r.sex}', '${r.birth}', '演示地址')`).join(',\n'))
  lines.push(") AS v(username, rt, sex, birth, addr) ON u.username = v.username;")
  lines.push('')

  // circulation history: per-copy sequential timelines on NEW copies only
  const now = new Date('2026-07-01T12:00:00Z').getTime()
  const day = 86400_000
  const loans = []
  const fines = []
  const copyCount = books.length * 2
  for (let c = 0; c < copyCount; c++) {
    if (rand() < 0.35) continue // some copies never circulate
    let cursor = now - 365 * day
    const rounds = 1 + Math.floor(rand() * 6)
    for (let i = 0; i < rounds; i++) {
      cursor += Math.floor(rand() * 40) * day
      const reader = readers[Math.floor(rand() * readers.length)]
      const loanDays = reader.type === 'TEACHER' ? 60 : 30
      const held = Math.floor((0.2 + rand() * 1.3) * loanDays) * day
      const loanedAt = cursor
      const dueAt = loanedAt + loanDays * day
      const returnedAt = loanedAt + held
      if (returnedAt > now - 2 * day) {
        // still out (possibly overdue right now)
        loans.push({ copySeq: c, username: reader.username, loanedAt, dueAt, returnedAt: null })
        break
      }
      loans.push({ copySeq: c, username: reader.username, loanedAt, dueAt, returnedAt })
      if (returnedAt > dueAt) {
        const days = Math.ceil((returnedAt - dueAt) / day)
        fines.push({ loanIdx: loans.length - 1, amount: days * 10, paid: rand() < 0.8 })
      }
      cursor = returnedAt
    }
  }

  const ts = (millis) => `'${new Date(millis).toISOString()}'`
  lines.push('-- 12-month circulation history (deterministic, seed 20260702)')
  lines.push(`WITH new_copies AS (
  SELECT c.id, row_number() OVER (ORDER BY c.id) - 1 AS seq
  FROM book_copies c WHERE c.book_id > 62
)`)
  lines.push('INSERT INTO loans (copy_id, reader_id, loaned_at, due_at, returned_at, operator_id)')
  lines.push('SELECT nc.id, u.id, v.loaned::timestamptz, v.due::timestamptz,')
  lines.push("       CASE WHEN v.returned = '' THEN NULL ELSE v.returned::timestamptz END,")
  lines.push("       (SELECT id FROM users WHERE username = 'admin')")
  lines.push('FROM (VALUES')
  lines.push(loans.map((l) =>
    ` (${l.copySeq}, '${l.username}', ${ts(l.loanedAt)}, ${ts(l.dueAt)}, ${l.returnedAt ? ts(l.returnedAt) : "''"})`).join(',\n'))
  lines.push(') AS v(seq, username, loaned, due, returned)')
  lines.push('JOIN new_copies nc ON nc.seq = v.seq')
  lines.push('JOIN users u ON u.username = v.username;')
  lines.push('')

  // mark still-out copies
  lines.push(`UPDATE book_copies SET status = 'ON_LOAN'
WHERE id IN (SELECT copy_id FROM loans WHERE returned_at IS NULL);`)
  lines.push('')

  // fines: locate the loan by copy seq + loaned_at
  if (fines.length > 0) {
    lines.push('INSERT INTO fines (loan_id, reader_id, amount_cents, reason, status, paid_at)')
    lines.push('SELECT l.id, l.reader_id, v.amount, \'OVERDUE\',')
    lines.push("       CASE WHEN v.paid THEN 'PAID' ELSE 'UNPAID' END,")
    lines.push('       CASE WHEN v.paid THEN l.returned_at ELSE NULL END')
    lines.push('FROM (VALUES')
    lines.push(fines.map((f) => {
      const l = loans[f.loanIdx]
      return ` (${l.copySeq}, ${ts(l.loanedAt)}, ${f.amount}, ${f.paid})`
    }).join(',\n'))
    lines.push(') AS v(seq, loaned, amount, paid)')
    lines.push(`JOIN (SELECT c.id, row_number() OVER (ORDER BY c.id) - 1 AS seq
      FROM book_copies c WHERE c.book_id > 62) nc ON nc.seq = v.seq`)
    lines.push('JOIN loans l ON l.copy_id = nc.id AND l.loaned_at = v.loaned::timestamptz;')
  }

  return { sql: lines.join('\n') + '\n', loanCount: loans.length, fineCount: fines.length, readerCount: readers.length }
}

