/**
 * Catalogue expansion generator.
 *
 * 1. Pulls real bibliographic records (title/author/publisher/year/ISBN-13)
 *    from the Open Library search API across CLC-mapped subjects.
 * 2. Emits backend/src/main/resources/db/migration/V5__catalog_expansion.sql:
 *    - ~300 real books + 2 copies each (barcodes from barcode_seq)
 *    - 34 fictional demo readers
 *    - a deterministic 12-month circulation history (loans / returns /
 *      overdue fines) confined to the NEW books × NEW readers, so the
 *      original demo dataset that integration tests rely on stays untouched.
 *
 * Deterministic: seeded PRNG; re-running against the same API responses
 * yields the same SQL. Usage: node tools/expand-catalog/generate.mjs
 */

