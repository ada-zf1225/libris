// mulberry32 — tiny seeded PRNG
function rng(seed) {
  return function () {
    seed |= 0; seed = (seed + 0x6d2b79f5) | 0
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed)
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296
  }
}
const rand = rng(20260702)
const pick = (arr) => arr[Math.floor(rand() * arr.length)]
const esc = (s) => String(s).replaceAll("'", "''")

async function fetchSubject(subject, categoryId, want) {
  const url = `https://openlibrary.org/search.json?q=subject:%22${encodeURIComponent(subject)}%22&fields=title,author_name,first_publish_year,isbn,publisher&limit=${want * 4}&sort=editions`
  const res = await fetch(url, { headers: { 'User-Agent': 'libris-catalog-builder (zf1225@ic.ac.uk)' } })
  if (!res.ok) throw new Error(`${subject}: HTTP ${res.status}`)
  const data = await res.json()
  const rows = []
  for (const doc of data.docs ?? []) {
    if (rows.length >= want) break
    const isbn13 = (doc.isbn ?? []).find((i) => /^97[89]\d{10}$/.test(i))
    if (!isbn13 || !doc.title || !doc.author_name?.length) continue
    if (doc.title.length > 200) continue
    rows.push({
      title: doc.title.trim(),
      author: doc.author_name.slice(0, 3).join(', '),
      publisher: (doc.publisher?.[0] ?? 'Unknown').slice(0, 120),
      isbn: isbn13,
      year: doc.first_publish_year && doc.first_publish_year > 1500 ? doc.first_publish_year : null,
      categoryId,
    })
  }
  return rows
}

